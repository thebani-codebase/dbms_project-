CREATE OR REPLACE PROCEDURE calculate_beneficiary_eligibility (
  p_beneficiary_id IN NUMBER
) AS
BEGIN
  DELETE FROM eligibility_match
  WHERE beneficiary_id = p_beneficiary_id;

  INSERT INTO eligibility_match (beneficiary_id, scheme_id, match_score)
  SELECT b.beneficiary_id,
         ec.scheme_id,
         100 AS match_score
  FROM beneficiary b
  JOIN eligibility_criteria ec
    ON b.age BETWEEN ec.min_age AND ec.max_age
   AND (ec.gender = 'ANY' OR ec.gender = b.gender)
   AND (ec.marital_status IS NULL OR ec.marital_status = b.marital_status)
   AND (ec.max_income IS NULL OR b.annual_income <= ec.max_income)
   AND (ec.caste_category IS NULL OR ec.caste_category = b.caste_category)
   AND (ec.bpl_required = 'N' OR b.bpl_flag = 'Y')
   AND (ec.disability_required = 'N' OR b.disability_flag = 'Y')
  WHERE b.beneficiary_id = p_beneficiary_id;

  COMMIT;
END;
/

BEGIN
  calculate_beneficiary_eligibility(1);
END;
