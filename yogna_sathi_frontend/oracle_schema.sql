-- Yogna Saathi Oracle DBMS Integration Script
-- Run in Oracle SQL Developer. This script demonstrates the real database
-- layer that the website would call through a Node.js/Express backend.

CREATE TABLE scheme_domain (
  domain_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  domain_name VARCHAR2(80) NOT NULL UNIQUE,
  ministry_name VARCHAR2(120),
  active_flag CHAR(1) DEFAULT 'Y' CHECK (active_flag IN ('Y', 'N'))
);

CREATE TABLE location (
  location_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  parent_location_id NUMBER,
  location_name VARCHAR2(100) NOT NULL,
  location_type VARCHAR2(30) CHECK (location_type IN ('STATE', 'DISTRICT', 'BLOCK', 'VILLAGE')),
  CONSTRAINT fk_location_parent FOREIGN KEY (parent_location_id)
    REFERENCES location(location_id)
);

CREATE TABLE occupation (
  occupation_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  occupation_name VARCHAR2(80) NOT NULL UNIQUE
);

CREATE TABLE policy_scheme (
  scheme_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  domain_id NUMBER NOT NULL,
  scheme_name VARCHAR2(160) NOT NULL,
  benefit_type VARCHAR2(80),
  launch_year NUMBER(4),
  CONSTRAINT fk_scheme_domain FOREIGN KEY (domain_id)
    REFERENCES scheme_domain(domain_id)
);

CREATE TABLE scheme_benefit (
  benefit_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  scheme_id NUMBER NOT NULL,
  benefit_amount NUMBER,
  benefit_frequency VARCHAR2(30),
  annual_value NUMBER,
  CONSTRAINT fk_benefit_scheme FOREIGN KEY (scheme_id)
    REFERENCES policy_scheme(scheme_id)
);

CREATE TABLE eligibility_criteria (
  criteria_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  scheme_id NUMBER NOT NULL,
  min_age NUMBER DEFAULT 0,
  max_age NUMBER DEFAULT 120,
  gender VARCHAR2(10) DEFAULT 'ANY',
  marital_status VARCHAR2(30),
  max_income NUMBER,
  caste_category VARCHAR2(30),
  bpl_required CHAR(1) DEFAULT 'N' CHECK (bpl_required IN ('Y', 'N')),
  disability_required CHAR(1) DEFAULT 'N' CHECK (disability_required IN ('Y', 'N')),
  occupation_required VARCHAR2(80),
  CONSTRAINT fk_criteria_scheme FOREIGN KEY (scheme_id)
    REFERENCES policy_scheme(scheme_id)
);

CREATE TABLE beneficiary (
  beneficiary_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  location_id NUMBER NOT NULL,
  occupation_id NUMBER,
  full_name VARCHAR2(120) NOT NULL,
  age NUMBER CHECK (age BETWEEN 0 AND 120),
  gender VARCHAR2(10),
  marital_status VARCHAR2(30),
  annual_income NUMBER,
  caste_category VARCHAR2(30),
  bpl_flag CHAR(1) DEFAULT 'N' CHECK (bpl_flag IN ('Y', 'N')),
  disability_flag CHAR(1) DEFAULT 'N' CHECK (disability_flag IN ('Y', 'N')),
  CONSTRAINT fk_beneficiary_location FOREIGN KEY (location_id)
    REFERENCES location(location_id),
  CONSTRAINT fk_beneficiary_occupation FOREIGN KEY (occupation_id)
    REFERENCES occupation(occupation_id)
);

CREATE TABLE document (
  document_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  beneficiary_id NUMBER NOT NULL,
  document_type VARCHAR2(80) NOT NULL,
  verified_flag CHAR(1) DEFAULT 'N' CHECK (verified_flag IN ('Y', 'N')),
  CONSTRAINT fk_document_beneficiary FOREIGN KEY (beneficiary_id)
    REFERENCES beneficiary(beneficiary_id)
);

CREATE TABLE eligibility_match (
  beneficiary_id NUMBER NOT NULL,
  scheme_id NUMBER NOT NULL,
  match_score NUMBER(5,2),
  calculated_on DATE DEFAULT SYSDATE,
  PRIMARY KEY (beneficiary_id, scheme_id),
  CONSTRAINT fk_match_beneficiary FOREIGN KEY (beneficiary_id)
    REFERENCES beneficiary(beneficiary_id),
  CONSTRAINT fk_match_scheme FOREIGN KEY (scheme_id)
    REFERENCES policy_scheme(scheme_id)
);

CREATE TABLE beneficiary_enrollment (
  enrollment_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  beneficiary_id NUMBER NOT NULL,
  scheme_id NUMBER NOT NULL,
  enrollment_status VARCHAR2(30) DEFAULT 'PENDING',
  applied_on DATE DEFAULT SYSDATE,
  CONSTRAINT fk_enrollment_beneficiary FOREIGN KEY (beneficiary_id)
    REFERENCES beneficiary(beneficiary_id),
  CONSTRAINT fk_enrollment_scheme FOREIGN KEY (scheme_id)
    REFERENCES policy_scheme(scheme_id)
);

CREATE TABLE awareness_campaign (
  campaign_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  location_id NUMBER NOT NULL,
  campaign_name VARCHAR2(140) NOT NULL,
  ngo_name VARCHAR2(120),
  campaign_cost NUMBER,
  start_date DATE,
  CONSTRAINT fk_campaign_location FOREIGN KEY (location_id)
    REFERENCES location(location_id)
);

CREATE TABLE campaign_impact (
  impact_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  campaign_id NUMBER NOT NULL,
  awareness_before NUMBER(5,2),
  awareness_after NUMBER(5,2),
  new_enrollments NUMBER,
  CONSTRAINT fk_impact_campaign FOREIGN KEY (campaign_id)
    REFERENCES awareness_campaign(campaign_id)
);

