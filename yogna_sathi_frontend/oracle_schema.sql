-- Citizen result shown on the website.
SELECT b.full_name,
       ps.scheme_name,
       sd.domain_name,
       sb.annual_value,
       em.match_score
FROM eligibility_match em
JOIN beneficiary b ON b.beneficiary_id = em.beneficiary_id
JOIN policy_scheme ps ON ps.scheme_id = em.scheme_id
JOIN scheme_domain sd ON sd.domain_id = ps.domain_id
JOIN scheme_benefit sb ON sb.scheme_id = ps.scheme_id
WHERE b.beneficiary_id = 1;

-- Government/Admin dashboard query shown in the website.
SELECT l.location_name,
       COUNT(em.scheme_id) AS eligible_not_enrolled,
       SUM(sb.annual_value) AS missed_benefit
FROM location l
JOIN beneficiary b ON b.location_id = l.location_id
JOIN eligibility_match em ON em.beneficiary_id = b.beneficiary_id
JOIN scheme_benefit sb ON sb.scheme_id = em.scheme_id
LEFT JOIN beneficiary_enrollment be
  ON be.beneficiary_id = b.beneficiary_id
 AND be.scheme_id = em.scheme_id
WHERE be.enrollment_id IS NULL
GROUP BY l.location_name
ORDER BY missed_benefit DESC;

